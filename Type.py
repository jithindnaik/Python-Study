# Type Operator

int_val = 50
float_val = 3.14
bool_val = True

print(type(int_val)) # <class 'int'>
print(type(float_val)) # <class 'float'>
print(type(bool_val)) # <class 'bool'>

# Str Operator

int_val = str(50)
float_val = str(3.14)
bool_val = str(True)

print(type(int_val)) # <class 'str'>
print(type(float_val)) # <class 'str'>
print(type(bool_val)) # <class 'str'>
